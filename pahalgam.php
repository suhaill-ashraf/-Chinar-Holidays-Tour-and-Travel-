                <p style="text-align: justify;">Situated over the Lidder River, Kolahoi glacier is a hanging glacier
                    known for its breath taking views. While the only way of reaching this glacier is though trekking,
                    one can also hire ponies or horses for some parts of the journey.
                </p>
