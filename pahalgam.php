                    Head-Quarter.
                </p>

                <h6>ARU VALLEY</h6>
                <p style="text-align: justify;">Located further up from Pahalgam (around 13 kilometers to be precise,
                    which takes around 30 minutes in a car) Aru Valley is a nature and animal lover’s paradise. Do visit
                    this place if you like to trek further high into the mountains, are an animal lover (which are in
                    abundance in this place) or a bird watcher. Every year hundreds of people from across the world come
                    to Aru valley for their bird watching fix. Besides, the place has a small market of its own where
                    you can buy authentic Kashmiri souvenirs to take back home. Do not forget to try some local food at
                    the small restaurants in Aru valley.
                </p>
                <h6>BAISARAN</h6>
                <p style="text-align: justify;">Baisaran is a beautiful destination 6 kms away from Pahalgam. With
