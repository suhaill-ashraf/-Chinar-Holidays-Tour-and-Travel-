                <h2 class=" bg-white text-start text-primary " style="text-decoration: underline;">SRINAGAR :</h2>
                <h6>DAL LAKE</h6>
                <p style="text-align: justify;">The famous Dal Lake is the second-largest lake in Jammu & Kashmir. The
                    urban lake, is integral to
                    tourism and recreation in Kashmir and is named the “Jewel in the crown of Kashmir” or “Srinagar’s
                    Jewel”. The shore line of the lake, is about 15.5 kilometres (9.6 mi), is encompassed by a boulevard
                    lined with Mughal era gardens, parks, houseboats and hotels. The wetland is divided by causeways
                    into four basins; Gagribal, Lokut Dal, Bod Dal and Nagin (although Nagin is also considered as an
                    independent lake). Lokut-dal and Bod-dal each have an island in the centre, known as Rup Lank (or
                    Char Chinari) and Sona Lank respectively.The houseboats are closely associated with Dal Lake which
                    provide accommodation to tourists in Srinagar.
                </p>
                <h6>INDIRA GANDHI MEMORIAL TULIP GARDEN</h6>
                <p style="text-align: justify;">Indira Gandhi Memorial Tulip Garden is the largest tulip garden in Asia
                    which is set in the foothills
                    of the Zabarwan mountain range in Srinagar. Formerly known as Model Floriculture Center, it
                    overlooks the mesmerising Dal Lake. The Tulip Festival held here is the highlight which attracts
                    tourists from the world over.
                </p>
                <h6>BADAMWARI GARDEN</h6>
                <p style="text-align: justify;">The early bloom of flowers on Almond trees in the sprawling historic
                    Badamwari garden in Shahr-e-Khaas has become a source of attraction for nature lovers and tourists,
                    alike. Situated on foothills of Koh-e-Maran, the 300 kanals garden was dotted with trees, flowers
                    and cascades providing a soothing experience to the visitors. Historians maintain there was no
                    record to suggest who laid the garden but they say it existed even before the rule of Sultan
                    Zain-ul-Abidin in the 14th century. A well covered dome in the garden is named after an Afghan
                    ruler, Warris Shah. The bloom on the Almond trees heralded the spring. the garden was revived and
                    formally thrown open for public in 2008
                </p>
                <h6>HARI PARBAT FORT</h6>
                <p style="text-align: justify;">Hari Parbat is also known as Kooh-e-Maran lies on the west of Dal Lake
                    in Srinagar, in the UT of Jammu & Kashmir. This Mughal structure was built by an Afghan Governor
                    Atta Mohammed Khan in 18th century. Later a long wall was constructed in 1590 by Emperor Akbar.
                    Surrounded by commendable structures from all religions, this fort sits at the top giving a
                    spectacular view of Dal Lake.
                </p>
                <p style="text-align: justify;">This fort is maintained by the Archaeological Survey of India (ASI) and
                    still stands impressive with old apartments and tall pillars. Hari Parbat provides a top view of
                    Makhdoom Sahib shrine.
                </p>
                <h6>PARI MAHAL</h6>
                <p style="text-align: justify;">Pari Mahal or The Angels’ Abode is a seven terraced garden located at
                    the top of Zabarwan mountain range over-looking city of Srinagar and south-west of Dal Lake The
                    architecture depicts an example of Islamic architecture and patronage of art during the reign of the
                    then Mughal Emperor Shah Jahan.

                </p>
                <p style="text-align: justify;"> The Pari Mahal was built by Mughal Prince Dara Shikoh in the mid-1600s.
                    It served as a library and an abode for him. Dara Shikoh was said to have lived in this area in the
                    years 1640, 1645, and 1654.
                    It was further used as an observatory, useful for teaching astrology and astronomy.
                    It is five-minute drive from Cheshmashahi Garden.
                </p>
                <h6>DACHIGAM NATIONAL PARK</h6>
                <p style="text-align: justify;"> <b>Dachigam National Park</b> is located 22 km (kilometer) from
                    Srinagar in
                    Jammu and Kashmir. It covers an area of 141 sq km (square kilometer). The name literally stands for
                    ‘ten villages’, which could be in memory of the ten villages that were relocated in order to create
                    the park.

                </p>
                <p style="text-align: justify;">Dachigam was initially established to ensure the supply of clean
                    drinking water to Srinagar city. A protected area since 1910, it was declared as a national park in
                    1981. The park is best known as the home of the hangul, or Kashmir stag. Dachigam is open throughout
                    the year, but the best time to visit is between April and August. The closest airport is at
                    Srinagar. Habitat to Kashmir Stag : Only area where Kashmir stag is found.
                </p>

                <h6>HARWAN GARDEN</h6>
                <p style="text-align: justify;"><b>Harwan Garden,</b> situated in Harwan Village (19 km) from Srinagar
                    is an
                    impressive, large and admired tourist spot. A canal, beautifully bordered with Chinar trees and
                    blossoming flower beds, starting from a lake just behind the garden, flows through the middle of the
                    garden.

                </p>
                <p style="text-align: justify;">The major draw of the Harwan garden is its natural loveliness and charm.
                    With greenery in the backdrop of snowy environment, this garden has big green grass lawns and
                    attracts visitors virtually towards this place.
                </p>
                <p style="text-align: justify;">Reachable by taxis within half an hour, the garden is perfect place for
                    picnics and excursions, also an ideal place for natures walk. It is the gateway to Dachi Gam
                    Wildlife sanctuary.
                </p>
