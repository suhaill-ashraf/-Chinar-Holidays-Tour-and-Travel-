                <h6>SHALIMAR BAGH</h6>
                <p style="text-align: justify;">Shalimar Bagh is a Mughal garden in Srinagar, linked through a channel
                    to the northeast of Dal Lake, on its right bank located on the outskirts of Srinagar city in Jammu
                    and Kashmir, India. Its other names are Shalimar Garden, Shalimar Bagh, Farah Baksh and Faiz Baksh,
                    and the other famous shore line garden in the vicinity is Nishat Bagh. The Bagh was built by Mughal
                    Emperor Jahangir for his wife Noor Jahan, in 1619. The Bagh is considered the high point of Mughal
                    horticulture. It is now a public park. This time it’s also called as crown of Srinagar.
                </p>

                <h6>NISHAT BAGH</h6>
                <p style="text-align: justify;"> Nishat Bagh is a terraced Mughal garden built on the eastern side of
                    the Dal Lake, close to Srinagar in the UT of Jammu and Kashmir, India. It is the second largest
                    Mughal garden in the Kashmir Valley. The largest in size is the Shalimar Bagh, which is also located
                    on the bank of the Dal Lake. ‘Nishat Bagh’ is Urdu, which means “Garden of Joy,” “Garden of
                    Gladness” and “Garden of Delight.

                </p>
                <p style="text-align: justify;">Located on the bank of the Dal Lake, with the Zabarwan Mountains as its
                    backdrop, Nishat Bagh is a garden of bliss that commands a magnificent view of the lake beneath the
                    snow-capped Pir Panjal mountain range that stands far away to the west of the valley. The Bagh was
                    designed and built in 1633 by Asif Khan, elder brother of Nur Jehan.
                </p>

                <h6>CHASHME SHAHI</h6>
                <p style="text-align: justify;"> Chashma Shahi or Chashma i Shahi (translation: the royal spring), also
                    called Chashma Shahi, is one of the Mughal gardens built in 1632 AD around a spring by Ali Mardan
                    Khan, a governor of Mughal emperor Shah Jahan as per the orders of the Emperor, as a gift for his
                    elder son Prince Dara Shikoh. The garden is located in the Zabarwan Range, near Raj Bhawan
                    (Governor‘s house) overlooking Dal Lake in Srinagar, Kashmir

                </p>
                <p style="text-align: justify;">The garden was constructed around the spring by the Mughal Governor Ali
                    Mardan Khan in 1632. It was commissioned by the Mughal emperor Shah Jahan for his eldest son, Dara
                    Sikoh. In the east of Chashma Shahi the Pari Mahal (Fairy Palace) lies where Dara Sikoh used to
                    learn astrology.The garden is 108 m long and 38 m wide and is spread over one acre of land. It is
                    the smallest garden among the three Mughal gardens of Srinagar; the Shalimar garden is the largest
                    and the Nishat garden is the second largest. All the three gardens were built at the right bank of
                    the Dal Lake, with Zabarwan mountains (Zabarwan Range) at the backdrop.
                </p>

                <h6>HAZRATBAL SHRINE</h6>
                <p style="text-align: justify;"> The Hazratbal Shrine is the holiest Muslim shrine and mosque in
                    Kashmir. It houses a relic known as Moi-e-Muqqadas which is believed to be a sacred hair of Prophet
                    Muhammad’s beard. Situated on the banks of Dal Lake facing the Nishat Bagh, it affords a picturesque
                    view of the lake and surrounding mountains.

                </p>


                <h6>SHANKARACHARYA TEMPLE</h6>
                <p style="text-align: justify;"> Shankaracharya temple is a beautiful Shiva temple situated at a height
                    of 1100 feet on a picturesque location on the Gopadari Hill on the South East of Srinagar. Home to
                    Shankaracharya when he visited Srinagar, the temple was built in 250 BC. Earlier known as the
                    Gopadri Temple, it was renamed by King Lalitaditya as Shankaracharya Temple in the sixth century.
                    The temple became more accessible after the Dogra ruler, Maharaja Gulab Singh, constructed the
                    stairs. It is believed to be the oldest shrine in Kashmir. The shrine offers magnificent views of
                    the snow covered peaks of the Pir Panjal range and the valley.

                </p>



                <h6>NIGEEN LAKE</h6>
                <p style="text-align: justify;"> The Nigeen lake is surrounded by a large number of willow and poplar
                    trees. Hence, it has come to be referred as a “nageena”, which means “the jewel in the ring”. The
                    word “nigeen” is a local variant of the same word.

                </p>

                <p style="text-align: justify;">The lake is located adjacent to the Hari Parbat hillock, to the west of
                    the Dal lake. To its north east lies the locality of Hazratbal, which is known for the famous
                    shrine.

                </p>
                <p style="text-align: justify;"> The lake is a major tourist attraction in Srinagar, known for its
                    relatively pristine waters as compared to the Dal lake. Houseboats and Shikaras are a usual sight.
                    Its also ideal for swimming, being deeper and less crowded than the Dal lake.

                </p>

                <h6>BOTANICAL GARDEN</h6>
                <p style="text-align: justify;"> Botanical Garden was set up in 1969. Situated around the base of a hill
                    overlooking Dal Lake. It contains many types of plants and vegetation. This garden has a collection
                    of about 150,00 ornamental plants and a huge collection of oak varieties. It also has a rare
                    collection of Kashmiri tropical plants.

                </p>
                <p style="text-align: justify;"> Many people from various areas of world visit this place. It has four
                    main divisions: the Plant Introduction Centre, the Research Section, the Recreational Garden and the
                    Botanical Garden.

                </p>

                

