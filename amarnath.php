                <p style="text-align: justify;">The distance between Jammu and Baltal can be covered by Taxis / Buses
                    available at Tourist Reception Centre, J & K / bus stand or one can come to Srinagar by air and then
                    reach Baltal by road. The road route followed is:
                <ul>

                    <li>Jammu </li>
                    <li>Banihal </li>
                    <li>Srinagar </li>
                    <li>Sonamarg </li>
                    <li>Baltal </li>
                </ul>
                </p>
                <p style="text-align: justify;">The journey from Jammu to Baltal, which is about 400 kms, passes through
                    beautiful valleys, waterfalls, and greenery all over gives you a new lease of life. A thrill, which
                    is obviously there in every pilgrim, gets enhanced while passing through this way owing to the
                    Darshan of Holy Cave.
                </p>
                <p style="text-align: justify;">Holy Cave is just 14 Kms from Baltal and the distance can be covered by
                    foot / Ponies. However, Dandies are also available for handicapped and old aged pilgrims.
                </p>
                <p style="text-align: justify;">The width of the pebbled (kuchha) road is a bit narrow as compared to
