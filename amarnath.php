                <h6>VIA PAHALGAM & BALTAL ROUTES</h6>

                <h6>VIA PAHALGAM</h6>

                <p style="text-align: justify;">Situated 96 Kms from Srinagar, Pahalgam is known all over the world for
                    its beauty. Lidder and Aru Rivers and high mountains kiss the valley. Good Hotels are available for
                    accommodation. Arrangements for free Langars (food kiosks) are also made by Non Government
                    Organisations at Nunwan Yatri Camp, 6 kms. short of Pahalgam. Pilgrims camp at Pahalgam for the
                    first night. Sheshnag – is actually a mountain which derives its name from its Seven Peaks,
                    resembling the heads of the mythical snake. The second night’s camp at Sheshnag overlooks the deep
                    blue waters of Sheshnag lake, and glaciers beyond. There are legends of love and revenge too
                    associated with Sheshnag, and at the camp these are narrated by campfires. The stillness of the
                    Himalayan night increases your inner joy. Once you take bath and enjoy the scenic view, life takes a
                    whole new meaning altogether. On the way to The Holy Cave, one comes across the Sangam of Amravati
                    and Panjtarni rivers. Some pilgrims take bath at Amravati near the Holy Cave before going for
                    Darshan. There are two smaller Shivlings one of Maa Parvati and other of Shri Ganesh. It may be
                    noted that after having early Darshan of the Shiv Linga at Holy Cave, one can return to Panjtarni
                    well in time the same day.
                </p>

